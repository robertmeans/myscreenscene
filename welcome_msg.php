<p class="welcomeText">Years and years ago I built a homepage designed with all the features I want available as soon as I open a browser window. Over time as technology evolved, my custom homepage began to fall apart across the various browsers until all I could use it with was Internet Explorer. Oh the horror!<br />
<br />
Finally I said, &quot;enough!&quot; and began what I knew would be a time consuming project; rebuilding my beloved homepage. Of course every last detail was a meticulous, pain staking endeavor but I emerged with something I was proud of. So proud in fact that I decided to make it available to everyone.<br />
<br />
MyScreenScene does not try to be all things to all people. It was designed by me for me. I only need the facts ma'am. Save the weather, Twitter and news feeds for another page.<br />
<br />
After you Register an account click on the link at the top, &quot;Edit Mode&quot; and start styling this to suit your needs. Once finished click on &quot;Home&quot; and all your changes will be saved. Make as many changes as often as you'd like. Your page will always be available as you left it.<br />
